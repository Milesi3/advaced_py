# ✔ Создайте вручную список с повторяющимися целыми числами.
# ✔ Сформируйте список с порядковыми номерами нечётных элементов исходного списка.
# ✔ Нумерация начинается с единицы.

original_list = [2, 3, 2, 5, 2, 7, 2, 9, 2]

odd_indices = [i+1 for i, num in enumerate(original_list) if num % 2 != 0]

print("Входной массив:", original_list)
print("Порядковые номера нечетных элементов:", odd_indices)
